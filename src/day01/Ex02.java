package day01;

public class Ex02 {
	public static void main(String[] args) {
		
		/*
		 escape문자자
		  - \n : enter(엔터)와 동일한 역할
		  - \t : tab(탭) 크기만큼 오른쪽으로 이동(window-8칸)
		  - \" : "(큰따옴표)를 출력할 때
		  - \\ : \(역슬러쉬) 출력할 때 
		 */
		
		/*
		 - " " : 쌍따옴표로 묶인 값은 문자열로 인식한다
		 */
		System.out.println("H\ne\nl\nl\no\n Java");
		System.out.println("1\t1234567\t12345678\t12");
		System.out.println("test \"123\" 456");
		System.out.println("C:\\Users\\springcloud\\java\\workspace\\day01");
	}
}
